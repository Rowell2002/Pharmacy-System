    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        
        int age,contact_number;
        String customer_id,title,first_name,last_name;

        if(txt_customer_id.getText().trim().isEmpty()|| txt_first_name.getText().trim().isEmpty()|| txt_last_name.getText().trim().isEmpty()|| txt_age.getText().trim().isEmpty()|| txt_contact_number.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter all details of the customer", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else
        {
            customer_id=txt_customer_id.getText();
            title=cmb_title.getSelectedItem().toString();
            first_name=txt_first_name.getText();
            last_name=txt_last_name.getText();
            age=Integer.parseInt(txt_age.getText());
            contact_number=Integer.parseInt(txt_contact_number.getText());
        
            CCustomers c1=new CCustomers();
            c1.addCustomer(customer_id, title, first_name, last_name, age, contact_number);
        }
        
        txt_customer_id.setText("");
        cmb_title.setSelectedIndex(0);
        txt_first_name.setText("");
        txt_last_name.setText("");
        txt_age.setText("");
        txt_contact_number.setText("");
    }//GEN-LAST:event_btn_addActionPerformed

