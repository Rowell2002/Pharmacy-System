    private void btn_viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewActionPerformed
        
        DefaultTableModel model = (DefaultTableModel) tbl_customers.getModel();
        model.setRowCount(0);


    try {
        Connection con = MDbConnection.createConnection();
        Statement st = con.createStatement();
        String query = "SELECT * FROM customer";
        ResultSet rs = st.executeQuery(query);

        int no = 1; // Row number
        
        while (rs.next()) {
            String customer_id = rs.getString("customer_id");
            String title = rs.getString("title");
            String first_name = rs.getString("first_name");
            String last_name = rs.getString("last_name");
            int age = rs.getInt("age");
            int contact_number = rs.getInt("contact_number");

//            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
//            String formattedExpireDate = sdf.format(expireDate);

            model.addRow(new Object[]{customer_id, title, first_name, last_name, age, contact_number});
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error retrieving data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btn_viewActionPerformed

